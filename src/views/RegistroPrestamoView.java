    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        if (txtNumeroDocumento.getText() == null || txtNumeroDocumento.getText().isBlank()) {
            JOptionPane.showMessageDialog(null, "El campo \"DNI\" es obligatorio");
            txtNumeroDocumento.requestFocus();
            return;
        }

        if (txtNombres.getText() == null || txtNombres.getText().isBlank()) {
            JOptionPane.showMessageDialog(null, "El campo \"Nombres\" es obligatorio");
            txtNombres.requestFocus();
            return;
        }
        
        if (txtApellidos.getText() == null || txtApellidos.getText().isBlank()) {
            JOptionPane.showMessageDialog(null, "El campo \"Apellidos\" es obligatorio");
            txtApellidos.requestFocus();
            return;
        }

        if (prestamoData.getIdLibro() == null) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un libro");
            return;
        }
        
        int respuesta = JOptionPane.showConfirmDialog(null, "¿Desea registrar el préstamo'", "Registrar", JOptionPane.YES_NO_OPTION);

        if (respuesta != JOptionPane.YES_OPTION) {
            return;
        }

        // asignamos los valores a la clase prestamo
        prestamoData.setNumeroDocumentoUsuario(txtNumeroDocumento.getText());
        prestamoData.setFechaPrestamo(LocalDateTime.now());
        
        prestamoData.setUsuario(new Usuario(prestamoData.getNumeroDocumentoUsuario(), txtNombres.getText(), txtApellidos.getText()));

        prestamoData.setEstado(Constantes.ESTADO_PRESTAMO_PRESTADO);

        // registramos los datos del prestamo
        Almacenamiento.instance().registrarPrestamo(prestamoData);
        
        btnVolverActionPerformed(null);
        
    }//GEN-LAST:event_btnRegistrarActionPerformed

