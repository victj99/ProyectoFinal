    private void txtNumeroDocumentoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtNumeroDocumentoFocusLost
        var dni = txtNumeroDocumento.getText().strip();
        var usuarioExistente = Almacenamiento.instance().getUsuarioPorDNI(dni);

        if (usuarioExistente != null) {
            txtNombres.setText(usuarioExistente.getNombres());
            txtNombres.setEnabled(false);

            txtApellidos.setText(usuarioExistente.getApellidos());
            txtApellidos.setEnabled(false);

            txtLibroBuscar.requestFocus();
        } else {
            txtNombres.setEnabled(true);
            txtApellidos.setEnabled(true);

            txtNombres.setText("");
            txtApellidos.setText("");
        }
    }//GEN-LAST:event_txtNumeroDocumentoFocusLost

    private void tblLibrosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblLibrosMouseClicked
        int row = tblLibros.getSelectedRow();

        Long id = Long.valueOf((String) librosTableModel.getValueAt(row, 0));

        prestamoData.setIdLibro(id);
        prestamoData.setLibro(Almacenamiento.instance().getLibroPorId(id));
    }//GEN-LAST:event_tblLibrosMouseClicked

