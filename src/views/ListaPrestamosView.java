    private void tblListaMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblListaMouseReleased
        int rowSelected = tblLista.getSelectedRow();
        String idString = (String) defaultTableModel.getValueAt(rowSelected, 0);

        try {
            var itemPrestamo = Almacenamiento.instance().getPrestamoPorId(Long.parseLong(idString));

            // si aún no se encuentra en estado prestado, activamos el boton de devolver
            if (itemPrestamo != null && itemPrestamo.getEstado().equals(Constantes.ESTADO_PRESTAMO_PRESTADO)) {
                btnDevolver.setEnabled(true);
            }
        } catch (Exception e) {
        }
        
    }//GEN-LAST:event_tblListaMouseReleased

