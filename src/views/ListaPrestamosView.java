    private String[] convertirPrestamoATableRow(Prestamo item) {
        String nombreLibro = item.getLibro() != null ? item.getLibro().getNombre() : "";

        return new String[] {item.getId() + "", nombreLibro, item.getFechaPrestamoStr(), item.getFechaDevolucionStr(), item.getEstado(), item.getObservacionDevolucion()};
    }

    private void filtrarTabla() {
        LocalDate fechaPrestamoDesde = null;
        LocalDate fechaPrestamoHasta = LocalDate.now();
        
        List<String[]> datosFiltrados = Almacenamiento.instance().getPrestamos().stream().filter((item) -> {
            var esFechaMayor = fechaPrestamoDesde == null ? true : AppUtils.isLocalDateBeforeOrEqual(item.getFechaPrestamo().toLocalDate(), fechaPrestamoDesde);
            var esFechaMenor = fechaPrestamoHasta == null ? true : AppUtils.isLocalDateBeforeOrEqual(item.getFechaPrestamo().toLocalDate(), fechaPrestamoHasta);

            if (!esFechaMayor && !esFechaMenor) {
                return false;
            }

            return true;
        }).map((item) -> convertirPrestamoATableRow(item)).toList();

        defaultTableModel.setRowCount(0);

        for (String[] item : datosFiltrados) {
            defaultTableModel.addRow(item);
        }
    }

