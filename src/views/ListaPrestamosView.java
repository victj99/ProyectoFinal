    private void btnDevolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDevolverActionPerformed
        String respuesta = JOptionPane.showInputDialog(null, "Ingrese observaciones si es necesario",
                "¿Desea registar la devolución del libro?", JOptionPane.QUESTION_MESSAGE);

       if (respuesta == null) {
           return;
       }

        int rowSelected = tblLista.getSelectedRow();
        String idString = (String) defaultTableModel.getValueAt(rowSelected, 0);

        try {
            Almacenamiento.instance().registrarDevolucionPrestamo(Long.parseLong(idString), respuesta);
            JOptionPane.showMessageDialog(null, "Datos actualizados");
            filtrarTabla();
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnDevolverActionPerformed

